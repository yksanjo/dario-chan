"""Companion display ‚ÄĒ prints Dario-chan's expression before each response."""

import sys


def print_dario(expression: str, text: str, mood: str = ""):
    """
    Print Dario-chan with expression and response text.

    This keeps the terminal clean ‚ÄĒ just prints art + text inline.
    """
    art = _get_expression_art(expression)
    print()
    print(art)
    if mood:
        print(f"  {mood}")
    if text:
        print()
        # Wrap and print as bubble
        _print_bubble(text)
    print()


def _print_bubble(text: str, width: int = 60):
    """Print text in a simple bubble."""
    words = text.split()
    lines = []
    current = ""
    for word in words:
        if len(current) + len(word) + 1 <= width:
            current = f"{current} {word}".strip()
        else:
            if current:
                lines.append(current)
            current = word
    if current:
        lines.append(current)

    max_w = max((len(l) for l in lines), default=0) + 2
    print(f"‚ē≠{'‚ĒÄ' * max_w}‚ēģ")
    for line in lines:
        print(f"‚Ēā {line.ljust(max_w - 2)} ‚Ēā")
    print(f"‚ēį{'‚ĒÄ' * max_w}‚ēĮ")


